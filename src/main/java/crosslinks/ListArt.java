package crosslinks;

public class ListArt {
	
	
		private String wikiid; 
		private String lang;
		
		//Constructor 
		public ListArt(){
			wikiid = null;
			lang = null;
		}
		
		public String getWikiid() {
			return wikiid;
		}

		public void setWikiid(String wikiid) {
			this.wikiid = wikiid;
		}

		public String getLang() {
			return lang;
		}

		public void setLang(String lang) {
			this.lang = lang;
		}

		

}
